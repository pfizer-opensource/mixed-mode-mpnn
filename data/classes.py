def validate_family(dataset, family):
    for family in families:
        members = families[family]
        for species in members:
            assert len(species) == 2, "%s has no class!" % species
            species = species[0]
            assert len(dataset[dataset['CNAME'] == species]) >= 1, "%s not found!" % species
    print("Validation Complete!")


def add_tags(dataset, families):
    for family in families:
        print("Adding Family: %s" % family)
        dataset[family] = 0
        for member, class_num in families[family]:
             dataset.loc[dataset['CNAME'] == member, family] = class_num


